const files = {};

(function browseObject(obj) {
  if (typeof obj === 'object') {
    if (Array.isArray(obj)) {
      obj.forEach(item => {
        browseObject(item);
      });
    } else if (obj !== null) {
      Object.keys(obj).forEach(key => {
        browseObject(obj[key]);
      });
    }
  } else if (typeof obj === 'string') {
    files[obj] = `sounds/${obj}.mp3`;
  }
})(samples);

